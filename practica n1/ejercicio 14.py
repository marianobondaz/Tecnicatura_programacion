# Un profesor de desea calcular el promedio de un alumono a lo largo de los cuatro parciales que rindio

parcial_1           = float(input('ingrese nota del primer parcial:'))
parcial_2           = float(input('ingrese nota del segundo parcial:'))
parcial_3           = float(input('ingrese nota del tercer parcial:'))
parcial_4           = float(input('ingrese nota del cuarto parcial:'))

calculo_promedio    = ( parcial_1 + parcial_2 + parcial_3 + parcial_4 ) /4

print('Promedio obtenido :', calculo_promedio)  